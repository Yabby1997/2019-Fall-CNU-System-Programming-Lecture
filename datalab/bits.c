/* 
 * bitOr - x|y using only ~ and & 
 *   Example: bitOr(6, 5) = 7
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 */
int bitOr(int x, int y) {
	return ~((~x)&(~y));
}
/* 
 * TMax - return maximum two's complement integer 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 4
 *   Rating: 1
 */
int tmax(void) {
	int i = 1;
	return (((~i)>>1)^(i<<31));
}
/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
  	//2's complement
	return (~x)+1;
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
	//declare pointer and range of valid bits
	int numberOfShift = n<<3;											//n*8
	int validBits = 0xff;												//1111 1111(2), we only need least significant 8 bits
	//shift right n bytes and return only valid bits
	return (x>>numberOfShift)&validBits;
}
/* 
 * float_abs - Return bit-level equivalent of absolute value of f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument..
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned float_abs(unsigned uf) {
	//return value and argument are unsigned but assume it as float.
	//values for decoding floating-point expressions
	int expValueOnes = 0x7f800000;										//0111 1111 1000 .... 0000(2)
	int fracValueOnes = 0x7fffff;										//0000 0000 0111 .... 1111(2) 
	int sBitOne = 0x80000000;											//1000 0000 .... 0000 0000(2)
	//if argument is "Special Value" and it is not infinity, it is NaN. so return argument.
	//if it is not, and it is negative(s bit = 1), make s bit 0 and return it.
	//if it is not, and it is positive(s bit = 0), there's nothing we should handle. so return argument.
 	if(((uf&expValueOnes) == expValueOnes) && ((uf&fracValueOnes) > 0))
		return uf;
	else{
		if((uf&sBitOne) == sBitOne)
			return uf^sBitOne;
		else
			return uf;
	}
}
/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
	//msb bits for judging positive or negative of each argument and the result of addition
	int xymsb = ((x+y)>>31)&1;
	int xmsb = (x>>31)&1;
	int ymsb = (y>>31)&1;
 	//if msb of result not equals to each msb of two operands, it means overflow
	return !((xymsb^xmsb)&(xymsb^ymsb));
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
*   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
	//declare pointer and range of target
	int targetPointer = n<<3;												//n*3
	int rangeOfTarget= 0xff;												//255(10) = 1111 1111(2)
	//abstraction(simplifying)
	int targetClearedOrigin = x&(x^(rangeOfTarget<<targetPointer));			//clear(make 0s) the range of target of target position
	int newByte = c<<targetPointer;											//make new byte fit in the cleared position
	//make target position cleared, and add new bytes.
	return targetClearedOrigin+newByte;
}
/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
*   Legal ops: !   << (n << 3)) | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
	//msb bits of arguments
	int xmsb = (x>>31)&1;
	int ymsb = (y>>31)&1;
	//complex logic abstraction
	int xMinusY = x+(~y+1);													//x+(-y) = x-y
	int msbComparison = !((xmsb^ymsb)&xmsb);								//if xmsb = 1, ymsb = 0 -> 0, else -> 1 
	int isMsbComparisonValid = xmsb^ymsb;									//if xmsb equals ymsb, it is not valid comparison
	int xBigger = !((xMinusY>>31)&1);										//if x - y overflows, it means x equals y or x is bigger than y
	int isXdifferY = !(!xMinusY);											//if x equals y -> 0, else -> 1
	//compare with msb if it is valid comparison. but if not, compare with x, y and equality of those two arguments 
	return (msbComparison&isMsbComparisonValid)+((!isMsbComparisonValid)&(xBigger&isXdifferY));
}
